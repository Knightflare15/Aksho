export async function deleteStudentStorageObjects(schoolId: string, studentId: string) {
  const prefix = `schools/${schoolId}/students/${studentId}/`;
  await storage.bucket().deleteFiles({ prefix, force: true });
}

export async function deleteAuthUserIfExists(uid: string) {
  try {
    await auth.deleteUser(uid);
  } catch (error) {
    const code = typeof error === "object" && error !== null && "code" in error
      ? String((error as { code?: unknown }).code)
      : "";
    if (code !== "auth/user-not-found") throw error;
  }
}
